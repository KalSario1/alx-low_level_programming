#include "3-calc.h"

/**
 * op_add - func to sum
 * @a: the first int
 * @b: 2nd int
 * Return: sum in int
*/

int op_add(int a, int b)
{
return (a + b);
}

/**
 * op_sub - func to sub
 * @a: 1st int
 * @b: 2nd int
 * Return: sub in int
*/

int op_sub(int a, int b)
{
return (a - b);
}

/**
 * op_mul - func to mul
 * @a: 1st int
 * @b: 2nd int
 * Return: mul to int
*/

int op_mul(int a, int b)
{
return (a * b);
}

/**
 * op_div - funct to div
 * @a: 1st int
 * @b: 2nd int
 * Return: div in int
*/

int op_div(int a, int b)
{
return (a / b);
}

/**
 * op_mod - funct to remind
 * @a: 1st int
 * @b: 2nd int
 * Return: div in int
*/

int op_mod(int a, int b)
{
return (a % b);
}
